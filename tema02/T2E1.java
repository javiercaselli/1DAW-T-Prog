package tema02;

public class T2E1 {

    public static void main (String[] args) {
        int x, y;
        x = 144;
        y = 999;

        // Suma
        int suma = x + y;
        System.out.println(suma);
        // Otra forma:
        // System.out.println(x + y);

        // Resta
        int resta = x - y;
        System.out.println(resta);

        // Multiplicación
        int mult = x * y;
        System.out.println(mult);

        // División
        float div = (float) x / (float) y;
        System.out.println(div);
    }
}
